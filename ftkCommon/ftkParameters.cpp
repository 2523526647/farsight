#include "ftkParameters.h"

namespace ftk
{

void Parameters::AddParameter(std::string name, Type type, std::string value)
{
	Parameter p;
	p.name = name;
	p.type = type;
	p.value = value;
	m_Parameters.push_back(p);
}

}  // end namespace ftk
